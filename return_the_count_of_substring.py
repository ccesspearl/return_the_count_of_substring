# Exercise 7: Return the count of a given substring from a string
# Write a program to find how many times substring “Emma” appears in the given string.

# Pseudocode

# Create a variable for given string
given_string = "Emma is good developer. Emma is a writer"

# Use .count() to count "Emma" 
count_emma_string= given_string.count("Emma")

# Print the results 
print("Given string:", given_string)
print("Emma appeared", count_emma_string, "times")